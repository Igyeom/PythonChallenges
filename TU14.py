# Exercise 2
s = "hello world"
print("Original string:")
print(s)
print("Skipping first character:")
print(s[1:])
print("Skipping last character:")
print(s[:-1])
print("Skipping every other character:")
print(s[::2])
print("Reversed string (via slicing):")
print(s[::-1])
